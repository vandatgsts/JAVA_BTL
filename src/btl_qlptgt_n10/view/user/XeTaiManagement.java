    private void tblPhuongTienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPhuongTienMouseClicked
        // TODO add your handling code here:
        DefaultTableModel tableModel = (DefaultTableModel) tblPhuongTien.getModel();
        Vector<Object> courseVector = (Vector<Object>) tableModel.getDataVector().elementAt(tblPhuongTien.getSelectedRow());
        XeTai xeTai = new XeTai();
        xeTai.setId(Integer.parseInt(courseVector.get(1).toString()));
        xeTai.setHangSanXuat(courseVector.get(3).toString());
        xeTai.setNamSanXuat(Integer.parseInt(courseVector.get(4).toString()));
        xeTai.setGiaBan(Float.parseFloat(courseVector.get(5).toString()));
        xeTai.setMau(courseVector.get(6).toString());
        xeTai.setTrongTai(Float.parseFloat(courseVector.get(7).toString()));
        
        txtId.setText(String.valueOf(xeTai.getId()));
        cboHangSanXuat.getModel().setSelectedItem(xeTai.getHangSanXuat());
        cboNamSanXuat.getModel().setSelectedItem(xeTai.getNamSanXuat());
        txtGiaBan.setText(String.valueOf(xeTai.getGiaBan()));
        cboMau.getModel().setSelectedItem(xeTai.getMau());
        txtTrongTai.setText(String.valueOf(xeTai.getTrongTai()));
    }//GEN-LAST:event_tblPhuongTienMouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (txtGiaBan.getText().trim().compareTo("") == 0) {
            JOptionPane.showConfirmDialog(null, "Giá bán không được để trống", "Lỗi cập nhật", JOptionPane.DEFAULT_OPTION, JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (txtTrongTai.getText().trim().compareTo("") == 0) {
            JOptionPane.showConfirmDialog(null, "Trọng tải không được để trống", "Lỗi cập nhật", JOptionPane.DEFAULT_OPTION, JOptionPane.ERROR_MESSAGE);
            return;
        }
        XeTai xeTai = new XeTai(
                Float.parseFloat(txtTrongTai.getText()),
                Integer.parseInt(txtId.getText()),
                cboHangSanXuat.getSelectedItem().toString(),
                Integer.parseInt(cboNamSanXuat.getSelectedItem().toString()),
                Float.parseFloat(txtGiaBan.getText()),
                cboMau.getSelectedItem().toString()
        );
        
        if (!xeTaiController.updateXeTaiById(xeTai)) {
            JOptionPane.showConfirmDialog(null, "Sửa thông tin xe tải thất bại", "Sửa thông tin xe tải", JOptionPane.DEFAULT_OPTION, JOptionPane.ERROR_MESSAGE);
            return;
        }
        JOptionPane.showConfirmDialog(null, "Sửa thông tin xe tải thành công", "Sửa thông tin xe tải", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE);
        try {
            UpdateTable();
        } catch (SQLException ex) {
            Logger.getLogger(XeTaiManagement.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

