    private void loadNamSanXuat() {
        int namHienTai = Calendar.getInstance().get(Calendar.YEAR);
        for (int i = namHienTai; i >= 1920; i--) {
            ((DefaultComboBoxModel) cboNamSanXuat.getModel()).addElement(Integer.toString(i));
        }
    }
