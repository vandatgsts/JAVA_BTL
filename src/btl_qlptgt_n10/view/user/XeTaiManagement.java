    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        
        ArrayList<XeTai> xeTais = (ArrayList<XeTai>) xeTaiController.getXeTaiByHangSanXuat(cboHangSanXuat.getSelectedItem().toString());
        DefaultTableModel defaultTableModel = (DefaultTableModel) tblPhuongTien.getModel();
        while (defaultTableModel.getRowCount() > 0) {
            defaultTableModel.removeRow(0);
        }

        for (int i = 0; i < xeTais.size(); i++) {
            XeTai xeTai = xeTais.get(i);
            defaultTableModel.addRow(new Object[]{
                i + 1,
                xeTai.getId(),
                "Xe tải",
                xeTai.getHangSanXuat(),
                xeTai.getNamSanXuat(),
                xeTai.getGiaBan(),
                xeTai.getMau(),
                xeTai.getTrongTai()
            });
        }
    }//GEN-LAST:event_jButton4ActionPerformed

